package org.alljoyn.triumph.view.result;

import java.io.IOException;

import javafx.fxml.FXML;
import javafx.fxml.FXMLLoader;
import javafx.scene.control.TextArea;
import javafx.scene.layout.AnchorPane;

public class BasicResultView extends AnchorPane {

	@FXML TextArea mResultArea;
	
	public BasicResultView(Object result) {

		// FXML load the fxml layer.
		FXMLLoader fxmlLoader = new FXMLLoader(getClass().
				getResource("BasicResultView.fxml"));

		// Root is defined by fx:root is FXML
		fxmlLoader.setRoot(this);
		fxmlLoader.setController(this);

		try {
			fxmlLoader.load();
		} catch (IOException e) {
			throw new RuntimeException(e);
		}
		
		mResultArea.setText(result.toString());
	}

}
